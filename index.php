<h4>1. Os Malfoy podiam não ter sido... Malfoy</h4>
<p>Nos primeiros manuscritos de Harry Potter e a Pedra Filosofal, Draco surge com os apelidos Smart, Spicks e Spungen. Só depois se consolidou com o nome icónico que reconhecemos, detestamos ou amamos, desprezamos ou admiramos: Draco Malfoy.</p>

<h4>2. O plano curricular de Hogwarts sofreu algumas alterações </h4>
<p>Nas primeiras versões de Harry Potter e a Pedra Filosofal, algumas disciplinas eram um pouco diferentes daquelas que hoje nos são tão familiares. "Herbologia", por exemplo, era "Herbalismo" (Herbalism). "Artes Divinatórias" era uma disciplina obrigatória para os alunos de primeiro ano – algo que teria sem dúvida complicado muito a vida de Hermione. E existia ainda outra disciplina obrigatória para o primeiro ano, que acabou por não ficar sequer no plano curricular final: a de "Monstros" (Beasts). </p> 
<div>
<img src="img/harrybrasil.jpg" width="40%" height="55%" class="img-fluid">
</div>
<h4>3. Muitos dos nomes das personagens têm significados escondidos</h4>
<p>Como provavelmente muitos já sabem, J. K. Rowling recorre frequentemente ao latim, por exemplo, para escolher o nome das personagens, dos sítios e dos feitiços. Alguns casos são bastante evidentes: "Severus" significa "severo" e "Lupin", "lobo". Outros escondem melhor a sua origem: é o caso de "Dumbledore", que, em Inglês Antigo, significa "abelha". Rowling explica que escolheu este apelido para o diretor de Hogwarts por o imaginar sempre a cantarolar baixinho para si mesmo. Já o seu primeiro nome, Albus, significa "branco" em latim.</p> 
<p>Outro caso que à primeira vista pode parecer uma escolha 'inocente', mas, numa segunda análise, tem muito a dizer sobre a personagem é o de Ludo Bagman, o chefe do Departamento de Jogos e Desportos Mágicos. O seu nome, em latim, traduz-se sensivelmente para "eu jogo": um aceno de cabeça para o seu passado como jogador de Quidditch, mas, também, para o pouco saudável gosto que tem pelas apostas. </p>
<p>Ficamos a perceber também por que motivo "Bellatrix" é um nome tão à medida da personagem: é a palavra latina para "guerreira". Esta feiticeira anuncia o poder que tem logo ao dizer o  seu primeiro nome. </p> 


 
<h4>4. O lema de Hogwarts é sábio - e cómico</h4>
<p>"Draco Dormiens Nunquam Titillandus" – o lema (ou motto) de Hogwarts tem uma sonoridade imponente. Mas é mais uma piscadela de olho em latim: significa "Nunca faças cócegas a um dragão adormecido". O que é, na realidade, um conselho muito sensato.</p>
 
<h4>5. Nicholas Flamel existiu mesmo </h4>
<p>Nicholas Flamel, o feiticeiro que criou a pedra filosofal e, por isso, conseguiu viver mais de 600 anos, juntamente com a mulher, não é apenas uma personagem. Flamel foi uma pessoa real que, apesar de nunca ter conseguido produzir uma pedra filosofal, fez dessa a missão da sua vida, depois de ter comprado um livro de alquimia. </p>

